x = 6
y = 2
z = 7

print(f"{x} mas {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} dividido entre {y} es igual a {x / y}")


# division al piso es decir se elimina el decimal y se coloca el entero pelon
print(f"{z} dividido al piso de {y} es igual {z // y}")
# modulo, es decir lo que sobra dentor de una division que no tiene cabida, se usa para conocner numero spares e impares
print(f"{z} modulo de {y} es igual a {z % y}")
print(f"{x} elevado a la {y} es igual a {x ** y}")  # elevado por si mismo
print(f"{x} elevado a la {3} es igual a {x ** 3}")
print(f"La raiz de {x} es igual a {x ** 0.5}")  # raiz cuadrada
